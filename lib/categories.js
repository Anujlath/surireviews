export function getCategorySlugByName(name) {
    return CATEGORY_NAME_TO_SLUG.get(name) || null;
}

export function getCategoryGroupByName(name) {
    if (!name) return null;
    return TRUSTPILOT_CATEGORY_GROUPS.find((group) => group.subcategories.includes(name)) || null;
}

export function getCategoryBySlug(slug) {
    return TRUSTPILOT_CATEGORIES.find((item) => item.slug === slug) || null;
}

export function getCategoryFamilyName(categoryName) {
    if (!categoryName) return '';
    const normalized = String(categoryName).trim();
    if (!normalized) return '';
    if (normalized.includes('&')) {
        return normalized.split('&')[0].trim();
    }
    if (normalized.includes(',')) {
        return normalized.split(',')[0].trim();
    }
    return normalized.split(' ')[0].trim();
}

export function getRelatedCategories(categoryName) {
    const family = getCategoryFamilyName(categoryName).toLowerCase();
    if (!family) return [];
    return TRUSTPILOT_CATEGORIES.filter((item) =>
        getCategoryFamilyName(item.name).toLowerCase() === family
    );
}
